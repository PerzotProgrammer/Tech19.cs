int i = 0;
int koniec = 25;
int zerowanie = 0;

//FOR
for(i = zerowanie; i <= koniec; i++)
{
    Console.Write(i + " ");
}
Console.WriteLine();


//WHILE
i = zerowanie;
while(i <= koniec)
{
    Console.Write(i + " ");
    i++;
}
Console.WriteLine();


//DO... WHILE
i = zerowanie;
do
{
    Console.Write(i + " ");
    i++;
} while (i <= koniec);



